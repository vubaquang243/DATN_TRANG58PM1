package com.seatech.tp.tttmua.form;

import org.apache.struts.action.ActionForm;

public class QLyTToanTienMuaCTietForm extends ActionForm {

    private String guid;

    private String so_gd;

    private String so_tien;

    private String ngay_tt;

    private String trang_thai_tt;
    
    private String nop_cham;


    public QLyTToanTienMuaCTietForm() {
        this.guid = "";
        this.so_gd = "";
        this.so_tien = "";
        this.ngay_tt = "";
    }

    public void setGuid(String guid) {
        this.guid = guid;
    }

    public void setSo_gd(String so_gd) {
        this.so_gd = so_gd;
    }

    public void setSo_tien(String so_tien) {
        this.so_tien = so_tien;
    }

    public void setNgay_tt(String ngay_tt) {
        this.ngay_tt = ngay_tt;
    }

    public String getGuid() {
        return guid;
    }

    public String getSo_gd() {
        return so_gd;
    }

    public String getSo_tien() {
        return so_tien;
    }

    public String getNgay_tt() {
        return ngay_tt;
    }

    public void setTrang_thai_tt(String trang_thai_tt) {
        this.trang_thai_tt = trang_thai_tt;
    }

    public String getTrang_thai_tt() {
        return trang_thai_tt;
    }

    public void setNop_cham(String nop_cham) {
        this.nop_cham = nop_cham;
    }

    public String getNop_cham() {
        return nop_cham;
    }
}
